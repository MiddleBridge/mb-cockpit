// Helper function to get random word
export function getRandomWord(): HSK4Word {
  const randomIndex = Math.floor(Math.random() * hsk4Vocabulary.length);
  return hsk4Vocabulary[randomIndex];
}

// Helper function to get random words for multiple choice
export function getRandomWords(count: number, excludeId?: number): HSK4Word[] {
  const available = hsk4Vocabulary.filter(word => word.id !== excludeId);
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

